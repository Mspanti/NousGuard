// The main Composable function for your app's theme
@Composable
fun NousGuardTheme(
    darkTheme: Boolean = isSystemInDarkTheme(), // Check if system dark theme is enabled
    // Dynamic color is available only on Android 12+ (API 31+)
    dynamicColor: Boolean = true, // Set to false if you don't want dynamic colors
    content: @Composable () -> Unit // The content of your UI that will apply this theme
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> { // Android 12+
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }
    val view = LocalView.current
    if (!view.isInEditMode) {
        // Apply status bar color based on theme
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = darkTheme
        }
    }

    // Apply the MaterialTheme with our defined color scheme and typography
    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography, // Uses the Typography defined in Type.kt
        content = content
    )
}